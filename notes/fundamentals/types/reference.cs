// O tipo string em C# é usado para armazenar uma sequência de caracteres.
// É um tipo de referência imutável.
string mensagem = "Olá, Mundo!";
Console.WriteLine(mensagem);

// O tipo object em C# é a base de todos os tipos, podendo armazenar qualquer tipo de dado.
// Isso permite atribuir valores de diferentes tipos a uma mesma variável.
object valor = 42;      // Armazena um número inteiro
valor = "Texto";        // Agora armazena uma string
Console.WriteLine(valor);
