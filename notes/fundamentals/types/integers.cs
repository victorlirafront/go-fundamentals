// O tipo byte em C# é um tipo de dado que armazena números inteiros não assinados (sem sinal).
// Isso significa que ele só armazena valores positivos (incluindo zero). Usa 8 bits (1 byte) de memória.
byte idade = 25; // 0 a 255

// O tipo short em C# é um tipo de dado que representa números inteiros assinados (com sinal).
// Ele usa 16 bits (2 bytes) de memória e permite armazenar valores de -32.768 a 32.767.
short temperatura = -15; // -32.768 a 32.767

// O tipo int em C# é o tipo mais usado para números inteiros com sinal.
// Ele utiliza 32 bits (4 bytes) de memória e suporta valores de -2.147.483.648 a 2.147.483.647.
int populacao = 100000; // -2.147.483.648 a 2.147.483.647

// O tipo long em C# é usado para armazenar números inteiros muito grandes, com sinal.
// Ele utiliza 64 bits (8 bytes) e representa valores de -9.223.372.036.854.775.808 a 9.223.372.036.854.775.807.
long distancia = 9223372036854775807L; // 64 bits
