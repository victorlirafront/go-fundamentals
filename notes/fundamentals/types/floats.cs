// O tipo float em C# é usado para armazenar números de ponto flutuante com precisão simples.
// Ele utiliza 32 bits (4 bytes) de memória e suporta cerca de 7 dígitos de precisão significativa.
float peso = 65.5f; // Suporta precisão de 7 dígitos

// O tipo double em C# é usado para armazenar números de ponto flutuante com precisão dupla.
// Ele utiliza 64 bits (8 bytes) de memória e suporta cerca de 15 a 16 dígitos de precisão significativa.
double altura = 1.75; // Suporta precisão de 15-16 dígitos

// O tipo decimal em C# é usado para representar números de ponto flutuante com alta precisão.
// Ele utiliza 128 bits (16 bytes) de memória e é ideal para cálculos financeiros.
// Suporta até 28-29 dígitos significativos.
decimal salario = 1200.75m; // Alta precisão, ideal para valores financeiros
